def search_encyclopedia(keyword: str) -> List[Dict]:
    """搜索百科内容"""
    keyword_lower = keyword.lower()
    results = []
    
    for scam_id, scam_data in SCAM_ENCYCLOPEDIA.items():
        score = 0
        matched_in = []
        
        # 检查名称
        if keyword_lower in scam_data["name"].lower():
            score += 10
            matched_in.append("name")
        
        # 检查描述
        if keyword_lower in scam_data["short_desc"].lower():
            score += 5
            matched_in.append("desc")
        
        # 检查关键词
        for kw in scam_data["keywords"]:
            if keyword_lower in kw.lower():
                score += 3
                if "keywords" not in matched_in:
                    matched_in.append("keywords")
                break
        
        # 检查防范技巧
        for tip in scam_data["prevention_tips"]:
            if keyword_lower in tip.lower():
                score += 2
                break
        
        if score > 0:
            results.append({
                "id": scam_data["id"],
                "name": scam_data["name"],
                "icon": scam_data["icon"],
                "color": scam_data["color"],
                "short_desc": scam_data["short_desc"],
                "risk_level": scam_data["risk_level"],
                "score": score,
                "matched_in": matched_in
            })
    
    # 按分数排序
    results.sort(key=lambda x: x["score"], reverse=True)
    return results


